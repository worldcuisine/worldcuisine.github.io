                    <h5 class="card-title">Cara Membuat</h5>
                    <ol>
                        <li>Campurkan bahan pretzel menjadi adonan.<br>
                            Dalam wadah, masukkan air hangat, garam, gula, dan aduk hingga larut. Lalu tambahkan ragi,
                            aduk dan diamkan 5 menit atau sampai ragi mulai berbuih. Setelah itu masukkan tepung, 2 sendok
                            makan minyak, dan aduk hingga menjadi adonan.</li>
                        <li>Bentukkan adonan seperti hati. <br>
                            Angkat adonan, lalu gunakan sisa minyak untuk mengolesi wadah dan taruh kembali adonan ke dalam wadah.
                            Tutupi wadah dengan bungkus plastik dan biarkan di tempat hangat selama 60 menit. Sambil menunggu
                            panaskan terlebih dahulu oven ke suhu 230 derajat celcius. <br>
                            Setelah mengembang, potong adonan menjadi 8 bagian dan gulung satu per satu menjadi seperti tali
                            panjang lalu putar kedua ujungnya. Simpulkan satu sama lain hingga seperti bentuk hati dan menjadi
                            bentuk pretzel.</li>
                        <li>Rebus adonan dalam air mendidih. <br>
                            Tambahkan soda kue ke dalam panci besar berisi air mendidih. Kemudian masukkan adonan pretzel dan
                            rebus setiap pretzel selama 30 detik per sisi. Angkat dan tiriskan.</li>
                        <li>Panggang dan sajikan. <br>
                            Kemudian pindahkan adonan pretzel ke loyang, olesi permukaan pretzel dengan telur, dan taburi
                            dengan garam. Masukkan ke dalam oven yang sudah dipanaskan dan panggang selama 10-15 menit atau
                            hingga berwarna cokelat keemasan. Sajikan deh!</li>
