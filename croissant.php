                <div class="card-body">
                    <h5 class="card-title">Cara Membuat</h5>
                    <ol>
                        <li>Campurkan air hangat, gula, garam, dan ragi di sebuah mangkuk besar. Biarkan selama 5-10 menit
                            hingga berbusa. Hangatkan susu (jangan sampai mendidih), lalu campurkan dengan tepung dan campuran
                            ragi. Kamu dapat menggunakan mixer roti maupun spatula.</li>
                        <li>Uleni adonan, pukul-pukuli adonan 8-10 kali sampai adonan berasa halus dan elastis.</li>
                        <li>Taruh adonan di dalam mangkok dan biarkan adonan membesar sampai 1-2 jam. Jangan lupa untuk
                            memberikan sedikit tepung di mangkok agar adonan mudah di ambil.</li>
                        <li>Setelah adonan membesar, ratakan adonan. Gunakan rolling pin bila diperlukan. Pastikan pinggiran adonan rata.</li>
                        <li>Setelah adonan diratakan, taruh mentega beku di tengah-tengah adonan. Lalu lipat adonan hingga mentega tertutup.
                            Ratakan lagi adonan tersebut. Ulangi sekali lagi. Penting sekali untuk memastikan mentega tidak meleleh sampai
                            waktu pemanggangan. Bila perlu, masukkan lagi dan diamkan di kulkas.</li>
                        <li>Untuk memberikan kilap pada kulit croissant, berikan sedikit egg wash. (Cara membuat egg wash:
                            pecahkan 2 telur. Ambil kuningnya saja, berikan 1 sendok teh air, lalu kocok).</li>
                        <li>Potong adonan menjadi segitiga sama kaki (dengan tinggi yang panjang).</li>
                        <li>Gulung adonan yang sudah diratakan dan dipotong-potong menjadi segitiga sama kaki. Awas,
                            gulungan tidak boleh terlalu regang maupun terlalu ketat.</li>
                        <li>Setelah kamu melakukan bagian tersulit dari membuat croissant (membuat adonannya), kamu tinggal
                            memanggang croissant.</li>
                        <li>Nyalakan dan panaskan oven ke 180 derajat celsius. Masukkan adonan yang sudah dibentuk,
                            lalu panggang selama 10 menit. Jangan biarkan oven terlalu panas karena akan membuat roti
                            menjadi terlalu kering.</li>
                        <li>Setelah 10 menit, ambil croissant dari oven. Croissant akan bewarna coklat ke kuning-kuningan (golden brown)
                            ketika sudah jadi. Jangan biarkan di oven terlalu lama karena akan menjadikan kering.</li>
                        <li>Lalu biarkan selama beberapa menit sebelum mengkonsumsinya, karena croissant yang baru dari oven itu akan sangat panas.</li>
                    </ol>
